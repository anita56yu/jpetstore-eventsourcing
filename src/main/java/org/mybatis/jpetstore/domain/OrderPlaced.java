package org.mybatis.jpetstore.event;

public class OrderPlaced extends IEvent {
  private String itemId;
  private int amount;
  private int eventId;

  public OrderPlaced(String itemId, int amount) {
    super(itemId, amount);
    this.itemId = itemId;
    this.amount = amount;
  }

  @Override
  public String getItemId() {
    return this.itemId;
  }

  @Override
  public int getAmount() {
    return this.amount;
  }

  @Override
  public int getAmountDiff() {
    return -1 * amount;
  }

  @Override
  public int getEventId() {
    return this.eventId;
  }

  @Override
  public void setEventId(int id) {
    this.eventId = id;
  }
}
